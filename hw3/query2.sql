SELECT T.FACILITY_NAME, T.ST, T.CARCINOGEN, db2.gse.ST_AsText(T.LOCATION_DATA),
CAST(db2gse.ST_Distance(T.LOCATION_DATA, db2gse.ST_Point(-73.116371, 40.913780, 1), 'STATUTE MILE') AS DECIMAL(10, 4)) AS DISTANCE
FROM CSE532.TRIFacility AS T
WHERE T.CARCINOGEN = 'YES'
AND db2gse.ST_Contains(db2gse.ST_Buffer(db2gse.ST_Point(-73.116371, 40.913780, 1), 10, 'STATUTE MILE'), T.LOCATION_DATA) = 1
ORDER BY DISTANCE
LIMIT 1
;

